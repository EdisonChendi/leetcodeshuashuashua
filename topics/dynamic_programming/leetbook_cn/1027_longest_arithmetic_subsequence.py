class Solution1:
    # TLE
    def longestArithSeqLength(self, nums: List[int]) -> int:
        # dp[i,j] = 1 + dp[k,i] if A[j]-A[i] = A[i] - A[k], j > i > k
        dp = collections.defaultdict(lambda: 2)
        seen = collections.defaultdict(list)
        for j in range(len(nums)):
            for i in range(j):
                nk = 2*nums[i]-nums[j]
                for k in seen[nk]:
                    if k < i:
                        dp[i, j] = max(dp[i, j], 1+dp[k, i])
            seen[nums[j]].append(j)
        return max(dp.values() or [2])


class Solution:
    def longestArithSeqLength(self, nums: List[int]) -> int:
        # dp[i, diff], ends with index i, with diff
        dp = collections.defaultdict(lambda: 1)
        res = min(len(nums), 2)
        for j, nj in enumerate(nums):
            for i in range(j):
                ni = nums[i]
                diff = nj - ni
                temp = dp[j, diff] = dp[i, diff] + 1
                res = max(res, temp)
        return res


